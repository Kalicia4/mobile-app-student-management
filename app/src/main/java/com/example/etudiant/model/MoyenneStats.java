package com.example.etudiant.model;

public class MoyenneStats {
    private double moyenneClasse;
    private double moyenneMin;
    private double moyenneMax;

    // getters et setters

    public double getMoyenneClasse() { return moyenneClasse; }
    public void setMoyenneClasse(double moyenneClasse) { this.moyenneClasse = moyenneClasse; }

    public double getMin() { return moyenneMin; }
    public void setMin(double moyenneMin) { this.moyenneMin = moyenneMin; }

    public double getMax() { return moyenneMax; }
    public void setMax(double moyenneMax) { this.moyenneMax = moyenneMax; }
}
